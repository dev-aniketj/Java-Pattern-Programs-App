package com.aniketjain.patternprogramsinjava;

public class SelectionModel {
    int value;
    String heading;

    public SelectionModel(int value, String heading) {
        this.value = value;
        this.heading = heading;
    }

    public int getValue() {
        return value;
    }

    public String getHeading() {
        return heading;
    }
}
